main() {
  //OPERADORS ARITIMÉTICOS (operadores binários)
  int a = 7;
  int b = 3;
  int resultado =  a + b;
  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);
  print( a + (b * a) - (b / a));

  //OPERADORES LÓGICOS

  bool c = true;
  bool d = false;

  print(c);
  print(d);
  print(c && d);
  print(c || d);
  print(c ^ d);
}